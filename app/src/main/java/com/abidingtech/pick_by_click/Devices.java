package com.abidingtech.pick_by_click;

public class Devices {

    String name,id;

    public Devices() {
    }

    public Devices(String name, String id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
